    const langLabels = { pt: 'PT', en: 'EN', es: 'ES' };
    let currentLang = 'pt';

    function applyLang(lang) {
      const t = translations[lang];
      // Text nodes
      document.querySelectorAll('[data-i18n]').forEach(el => {
        if (t[el.dataset.i18n] !== undefined) el.textContent = t[el.dataset.i18n];
      });
      document.querySelectorAll('[data-i18n-html]').forEach(el => {
        if (t[el.dataset.i18nHtml] !== undefined) el.innerHTML = t[el.dataset.i18nHtml];
      });
      // Update typing words
      if (window.__setTypingWords) window.__setTypingWords(t['hero.typing']);
      // Update lang button label
      document.getElementById('langToggleBtn').textContent = langLabels[lang];
      // Mark active option
      document.querySelectorAll('.lang-option').forEach(o => {
        o.classList.toggle('active', o.dataset.lang === lang);
      });
      // Set html lang attribute
      document.documentElement.lang = lang === 'pt' ? 'pt-BR' : lang;
      currentLang = lang;
      try { sessionStorage.setItem('ag-lang', lang); } catch (e) { }
    }

    // ── LANG SWITCHER UI ─────────────────────────────────────────────────────────
    (function () {
      const panel = document.getElementById('langPanel');
      const btn = document.getElementById('langToggleBtn');

      // Restore saved lang
      try {
        const saved = sessionStorage.getItem('ag-lang');
        if (saved && translations[saved]) applyLang(saved);
      } catch (e) { }

      btn.addEventListener('click', e => {
        e.stopPropagation();
        panel.classList.toggle('open');
      });

      document.querySelectorAll('.lang-option').forEach(opt => {
        opt.addEventListener('click', () => {
          applyLang(opt.dataset.lang);
          setTimeout(() => panel.classList.remove('open'), 150);
        });
      });

      document.addEventListener('click', () => panel.classList.remove('open'));
    })();

